    private void meat_comboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_meat_comboActionPerformed


        String slct = meat_combo.getSelectedItem().toString();

       price prc = new price();
       prc.price();
        
        if (slct.equals("CHICKEN")){
            meat_quantity.setText("6");
            meat_price.setText("");
        }

        else if (slct.equals("BEEF BACHYA (Hadi)")){
            meat_quantity.setText("5");
            meat_price.setText(prc.beef_bachya_hadi_r);
        }

        else if (slct.equals("BEEF BACHYA (bagair hadi)")){
            meat_quantity.setText("6");
            meat_price.setText(prc.beef_bachya_bagair_hadi_r);
        }
         else if (slct.equals("QEEMA BACHYA")){
            meat_quantity.setText("6");
            meat_price.setText(prc.beef_bachya_bagair_hadi_r);
        }

        else if (slct.equals("BEEF COW (Hadi)")){
                        meat_quantity.setText("6");
                        meat_price.setText(prc.beef_bara_bagair_hadi_r);
        }

        else if (slct.equals("BEEF COW (bagair hadi)")){
            
                        meat_quantity.setText("6");
                        meat_price.setText(prc.beef_bara_hadi_r);
        }
        else if (slct.equals("QEEMA COW")){
            
                        meat_quantity.setText("6");
                        meat_price.setText(prc.beef_bara_hadi_r);
        }

        else if (slct.equals("NONE")){
            
                        meat_quantity.setText("6");
                        meat_price.setText("00");
        }

    }//GEN-LAST:event_meat_comboActionPerformed

